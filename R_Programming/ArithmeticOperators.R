# Arithmetic Operators

x <- 5
y <- 16
x+y
# Ans -> 21
x-y
# Ans -> -11
x*y
# Ans -> 80
y/x
# Ans -> 3.2
y%/%x
# Ans -> 3
y%%x
# Ans -> 1
y^x
# Ans -> 1048576

## Relational Operators

x <- 5
y <- 16
x<y
# Ans ->  TRUE
x>y
# Ans ->  FALSE
x<=5
# Ans ->  TRUE
y>=20
# Ans ->  FALSE
y == 16
# Ans ->  TRUE
x != 5
# Ans ->  FALSE

## Logical Operators

x <- c(TRUE,FALSE,0,6)
y <- c(FALSE,TRUE,FALSE,TRUE)
!x
# Ans ->  FALSE  TRUE  TRUE FALSE
x&y
# Ans ->  FALSE FALSE FALSE  TRUE
x&&y
# Ans ->  FALSE
x|y
# Ans ->   TRUE  TRUE FALSE  TRUE
x||y
# Ans ->  TRUE

## Assignment Operator

x <- 5
x
# Ans ->  5
x = 9
x
# Ans -> 9
10 -> x
x
# Ans ->  10
